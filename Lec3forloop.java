package javapractice;

public class Lec3forloop {

	public static void main(String[] args) {
		// for loop print 0 to 9
		for (int i = 0; i <=9; i++) {
			System.out.println(i);
		}
         // print 17 to 23
		for (int j =17; j <=23; j++) {
			System.out.println(j);
		}
		
		// find odd number between 10 to 20
		for (int k=11; k<20;k+=2) {
			System.out.println(k);
		}
		
		//Assignment
	   //1. Print odd number from 23 to 30
		for (int n=23; n<=30; n+=2) {
			System.out.println("odd number"+n);
		}
	   // 2. print even number from 23 to 30
		for (int k=24; k<=30; k+=2) {
			System.out.println("even number"+k);
		}
	}
}

